public abstract class Response extends Command{

    private Command command;

    //==================================================================================================================
    // Constructor(s)
    //==================================================================================================================

    public Response() {
    }

    public Response(int id, String name, int size, JInteger jId, Command command) {
        super(id, name, size, jId);
        this.command = command;
    }

    //==================================================================================================================
    // Getter(s) & Setter(s)
    //==================================================================================================================

    public Command getCommand() {
        return command;
    }

    public void setCommand(Command command) {
        this.command = command;
    }


    //==================================================================================================================
    // Public Functions(s)
    //==================================================================================================================


    //==================================================================================================================
    // Private Functions(s)
    //==================================================================================================================

}/*********************************************END OF FILE*************************************************************/

