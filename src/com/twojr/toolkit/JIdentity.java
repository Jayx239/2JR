public abstract class JIdentity implements IPrintable{

    private int id;
    private String name;

    //==================================================================================================================
    // Constructor(s)
    //==================================================================================================================
    public JIdentity() {
    }

    public JIdentity(int id, String name) {
        this.id = id;
        this.name = name;
    }


    //==================================================================================================================
    // Getter(s) & Setter(s)
    //==================================================================================================================
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


    //==================================================================================================================
    // Public Functions(s)
    //==================================================================================================================



    //==================================================================================================================
    // Private Functions(s)
    //==================================================================================================================

} /*********************************************END OF FILE*************************************************************/
